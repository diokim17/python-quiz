## 5번 다음 코드를 실행해보고 \t와 \n의 역할을 설명해보시오. print("안녕하세요. \n만나서\t\t반갑습니다.")
# >> 안녕하세요.
#    만나서    반가워요.
# \n은 줄 바꿈을 실행하고, \t는 공백, 띄어쓰기를 나타냄 -> 여기서 공백은 스페이스 바가 아닌 탭으로 실행


